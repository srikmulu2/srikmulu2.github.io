								<b> <A NAME="SN"> When is Sharada Navarathri Pooja performed?</A> </b> <a href="#BK">Top</A><br>
                                Navarathri Pooja is performed every year at the beginning of winter (Sept - Oct), the pooja is performed twice daily for nine days, each day one of the forms of Goddess is worshipped with Chatushasti upachara pooja (serving god with 64 upachara sevas) in morning & evening and respective homas are preformed, the navarathri pooja is concluded with Anna Daanam at the end.<br><br>

								<b>What is the significance of performing Sharada Navarathri Pooja?</b><br>
								Every day there are two time periods that are ideal for upasana (spiritual practice), first being Bramhi Kaalam (early morning between 4 - 5 Am, just before sunrise) and second is Pradosha Kaalam (evening 5 - 6 Pm, just before sunset). During these time periods the mind can be put under control easily, during other times of the day, we are under the control of our minds :-). The significance of performing Sharada Navaratri pooja is that during these nine days unlike the other days in the year, the mind can be easily put under control through out the day. By performing the Navarathri pooja, Goddess Sharada will help us control our mind and in turn control our Ego & Pride.<br>
